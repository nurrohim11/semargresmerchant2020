package gmedia.net.id.semargres2020merchant.volunteer.kirimkupon;

public class MerchantModel {
    String id,nama,email,deskripsi, kategori,alamat, latitude, longitude;
    public MerchantModel(String id, String nama,String email,String deskripsi, String kategori,String alamat,String  latitude, String longitude){
        this.id =id;
        this.nama =nama;
        this.alamat = alamat;
        this.email = email;
        this.deskripsi = deskripsi;
        this.kategori = kategori;
        this.latitude = latitude;
        this.longitude = longitude;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }
}
